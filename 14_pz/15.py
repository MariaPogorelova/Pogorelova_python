# В двумерном списке найти суммы элементов каждого столбца и поместить их в
# новый массив. Выполнить замену элементов второй строки исходной матрицы на
# полученные суммы.
### Вариант 15

import random

# Создание случайной матрицы
matrix = [[random.randint(-10, 10) for _ in range(4)] for _ in range(4)]

# Нахождение сумм элементов каждого столбца
col_sums = [sum(row[j] for row in matrix) for j in range(len(matrix[0]))]

# Замена элементов второй строки на суммы столбцов
matrix[1] = col_sums

print("Исходная матрица после преобразования:")
for row in matrix:
    print(row)